#include <stdio.h>

int main() {
    printf("\n");   /* newline */
    printf("\t");   /* tab */
    printf("\b");   /* backspace */
    printf("\r");   /* carriage return */
    printf("\\");   /* backslash */
    printf("\'");   /* single quote */
    printf("\"");   /* double quote */
    printf("\?");   /* question mark */
    printf("\a");   /* alert (bell) */
    printf("\f");   /* form feed */
    printf("\v");   /* vertical tab */

    return 0;
}